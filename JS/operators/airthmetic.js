//increment

var x = 10;

console.log("difference", ++x);
x++; //post-increment
console.log(x);

var y = 10;
++y; //pre-increment
console.log(y);

//decrement

var a = 20;
console.log(a--); //first it gives value then it's decremented
console.log(--a); //decrementes first and then givesthe value
