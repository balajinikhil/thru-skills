//Comparission Operator

//1.Equality ==
//2.Greater than >
//3.Greater than >=
//4.Less than <
//5.Less than equal to <=
//6.Strict Equality ===
//7.Not Equal !=
//8.Strict not equal to !==

var a = 10;
var b = 11;
console.log(a == b);

//greater than
console.log(a > b);

console.log(a >= b);

//less than
console.log(15 < 55);

console.log(10 <= 77);

//not equals to

console.log("Not equal to", 10 != 10);

//strict not equal
var x = 10;
var y = "10";
console.log("Strict not equal to", x === y);

console.log(10 === "10");
