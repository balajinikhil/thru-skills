//Assigment operators

//1.Assigment =
//2.Addition Assigment +=
//3.Substraction Assignment -=
//4.Multiplication Assignment *=
//5.Division Assignment /=
//6.Modulus Assignment %=

var x = 12;
console.log("value of x", x);

// x = x + 10;
x += 10;
console.log(x);

//x = x-10;
x -= 10;
console.log(x);

//x = x*10;
x *= 10;
console.log(x);

//x = x/10;
x /= 10;
console.log(x);

//x = x%10;
x %= 10;
console.log(x);
